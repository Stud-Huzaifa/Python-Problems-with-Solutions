#Can we have a set with 18 (int) and '18' (str) as a value in it?


'''Answer:
Yes, a set can contain both 18 (integer) and '18' (string) 
as they are considered different elements due to their differing data types'''

# Creating a set with both integer and string '18'
my_set = {18, '18'}

# Displaying the set
print(my_set)
